def area(width, height):
    result = width * height
    return result

result = area(5,6)
result = area(8,8)
result = area(10,8)
print(result)

def subtract(num1,num2):
    result = num1 - num2
    return result
result = subtract(10,2)
result = subtract(6,2)
print(result)

